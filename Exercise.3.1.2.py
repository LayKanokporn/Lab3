# First In last out (FILO) 
Array_A = [ ] 
print(Array_A) 
Array_A.append("A")
 # append “A” into Array_A  in 0 position
print(Array_A) 
Array_A.append("B")
 # append “B” into Array_A  in 1 position
print(Array_A) 
Array_A.append("C")
 # append “C” into Array_A in 2 position
print(Array_A) 
Array_A.append("D") 
# append “D” into Array_A in 3 position
print(Array_A) 
Array_A.pop(-1) 
# position -1 pop out D and Pop out “D” from Array_A 
print(Array_A) 
