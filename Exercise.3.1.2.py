# First In last out (FILO) 
Array_A = [ ] 
print(Array_A) 
Array_A.append("A") # Push “A” into Array_A 
print(Array_A) 
Array_A.append("B") # Push “B” into Array_A 
print(Array_A) 
Array_A.append("C") # Push “C” into Array_A 
print(Array_A) 
Array_A.append("D") # Push “D” into Array_A 
print(Array_A) 
Array_A.pop(-1) # Pop “D” from Array_A 
print(Array_A) 
Array_A.pop(-1) # Pop “C” from Array_A 
print(Array_A) 
Array_A.pop(-1) # Pop “B” from Array_A 
print(Array_A) 
